# 1. Acesse a pasta do projeto
cd /caminho/para/o/projeto

# 2. Verifique o status dos arquivos modificados
git status

# 3. Adicione os arquivos desejados ao staging
git add .

# 4. Faça um commit com uma mensagem descritiva
git commit -m "Descreva aqui a alteração realizada"

# 5. Envie para o repositório remoto (ajuste o nome da branch se necessário)
git push origin main

# 6. Se estiver em uma nova bracnh, faça o push com o comando abaixo
git push --set-upstream origin nome-da-branch

